<h1>Project Assignment for the PHP Course @ SoftUni</h1>

<h2>Requirements</h2>
<ul>
    <li><strong>Use PHP</strong> – the major part of your work should be PHP written
        <ul>
            <li>You should create custom PHP Framework</li>
            <li>You should create the project over your framework</li>
            <li>You must additionally use <strong>HTML5</strong>, <strong>CSS3</strong> to create the content and to stylize your web application</li>
            <li>You may optionally use <strong>JavaScript</strong>, <strong>jQuery</strong>, <strong>Bootstrap</strong></li>
            <li>Use <strong>PHP 5</strong></li>
        </ul>
    </li>
    <li><strong>User source control system</strong>
        <ul>
            <li><strong>Use GitHub</strong> or other source control system as project collaboration platform</li>
        </ul>
    </li>

    <li>Publish your project <strong>live in Internet</strong> – your project should be public in Internet
        <ul>
            <li>You may <strong>share your project</strong> to get external feedback</li>
            <li>Most shared and commented projects will get additional <strong>bonus score</strong></li>
        </ul>
    </li>
    <li><strong>Valid and high-quality PHP, HTML and CSS</strong>
        <ul>
            <li>Follow the best practices fr PHP development: http://www.phptherightway.com.</li>
            <li>Validate (when possible) your HTML (http://validator.w3.org) and CSS code (http://css-validator.org)</li>
            <li>Follow the best practices for <strong>high-quality PHP, HTML and CSS</strong>: good formatting, good code structure, consistent naming etc.</li>
        </ul>
    </li>
    <li><strong>Usability, UX and browser support</strong>
        <ul>
            <li>Your web application should be easy-to-use, with intuitive UI, with good usability</li>
            <li>Ensure your web application works correctly in the latest HTML5-compatible browsers: Chrome, Firefox, IE, Opera, Safari (latest versions, desktop and mobile versions)</li>
            <li>You do not need to support old browsers like IE9</li>
        </ul>
    </li>
</ul>

<h2>Forbidden Techniques and Tools</h2>

<ul>
    <li>Using <strong>CMS / blog systems</strong> (like WordPress, Drupal and Joomla) is forbidden.</li>
    <li>Using <strong>forum systems</strong> (like phpBB) is forbidden.</li>
    <li>Using <strong>photo album systems</strong> (like Plogger) is forbidden.</li>
    <li>Using <strong>audio album systems</strong> (like kPlaylist) is forbidden.</li>
    <li>Using <strong>Laravel</strong>, <strong>Zend Framework</strong>, <strong>CakePHP</strong> or other PHP MVC Framework is forbidden.</li>
</ul>