<h1>Blog</h1>

<h2><strong>Required</strong> functionalities</h2>

<ul>
    <li>User registration / login and user profiles.</li>
    <li><strong>View</strong> all posts (optionally with paging).</li>
    <li>Adding <strong>new posts</strong> by the blog owner (after login or password protected). Each post must have <strong>tags</strong>.</li>
    <li>Adding <strong>comments</strong> for every post by visitors - each visitor must fill out his name, email (optionally) and comment text.</li>
    <li>Implement a sidebar holding a <strong>list of posts</strong> sorted by month / year / etc. and a list of the <strong>most popular tags</strong>.</li>
    <li><strong>Counter</strong> of visits for each post.</li>
    <li>Functionality for <strong>searching</strong> by tags.</li>
    <li>Use a <strong>database</strong> (like MySQL or MongoDB) or cloud-based data storage (like MongoLab and RedisLab).</li>
</ul>

<h2><strong>Optional</strong> functionalities:</h2>

<ul>
    <li><strong>Admin panel</strong>: add / edit / delete posts, comments, tags, etc.</li>
</ul>